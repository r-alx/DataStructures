#get an input from the user and print the relevent day For example if number entered 1 The day is Monday and so on.
kl = int(input("enter the num:"))
if kl == 1:
    print("today is monday")
elif kl == 2:
    print("today is tuesday")
elif kl == 3:
    print("today is wednesday")
elif kl == 4:
    print("today is thursday")
elif kl == 5:
    print("today is friday")
elif kl == 6:
    print("today is saturday")
elif kl == 7:
    print("today is sunday")
else:
    print("<<<Invalid input>>>")